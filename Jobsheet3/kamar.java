public class kamar {
    String type;
    String payment;
    int harga;
    int banyakTamu;
    int nomorKamar;
    
    kamar(String type, String payment, int harga, int banyakTamu, int nomorKamar){
        this.type = type;
        this.payment = payment;
        this.harga = harga;
        this.banyakTamu = banyakTamu;
        this.nomorKamar = nomorKamar;
    }
}